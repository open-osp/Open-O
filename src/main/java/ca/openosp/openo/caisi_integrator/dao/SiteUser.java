    /**
     * Checks whether this entity instance is in a detached state.
     *
     * <p>This method is part of the OpenJPA {@link PersistenceCapable} interface and determines
     * whether this instance is detached from its persistence context. A detached entity was
     * previously managed but is no longer associated with an active persistence context.</p>
     *
     * <p>The detection logic follows these rules:</p>
     * <ul>
     *   <li>If a state manager is present, delegate to its detachment status</li>
     *   <li>If detached state is set (and not DESERIALIZED), return true</li>
     *   <li>If an ID is present without a state manager, assume detached</li>
     *   <li>Returns null if the detachment state cannot be definitively determined</li>
     * </ul>
     *
     * @return Boolean true if detached, false if attached, null if indeterminate
     */
    public Boolean pcIsDetached() {
        if (this.pcStateManager != null) {
            if (this.pcStateManager.isDetached()) {
                return Boolean.TRUE;
            }
            return Boolean.FALSE;
        }
        else {
            if (this.pcGetDetachedState() != null && this.pcGetDetachedState() != PersistenceCapable.DESERIALIZED) {
                return Boolean.TRUE;
            }
            if (this.id != null) {
                return Boolean.TRUE;
            }
            if (!this.pcisDetachedStateDefinitive()) {
                return null;
            }
            if (this.pcGetDetachedState() == null) {
                return Boolean.FALSE;
            }
            return null;
        }
    }
    
    private boolean pcisDetachedStateDefinitive() {
        return false;
    }
    
    public Object pcGetDetachedState() {
        return this.pcDetachedState;
    }
    
    public void pcSetDetachedState(final Object pcDetachedState) {
        this.pcDetachedState = pcDetachedState;
    }
    
    private void writeObject(final ObjectOutputStream objectOutputStream) throws IOException {
        final boolean pcSerializing = this.pcSerializing();
        objectOutputStream.defaultWriteObject();
        if (pcSerializing) {
            this.pcSetDetachedState(null);
        }
    }
    
    private void readObject(final ObjectInputStream objectInputStream) throws IOException, ClassNotFoundException {
        this.pcSetDetachedState(PersistenceCapable.DESERIALIZED);
        objectInputStream.defaultReadObject();
    }
